operation = input("pick action +, -, //, /, *, **, %:")

if operation  == "+":
    nr1 = int(input("write nr 1:"))
    nr2 = int(input("write nr 2:"))
    print(nr1 + nr2)

if operation  == "-":
    nr1 = int(input("write nr 1:"))
    nr2 = int(input("write nr 2:"))
    print(nr1 - nr2)

if operation  == "*":
    nr1 = int(input("write nr 1:"))
    nr2 = int(input("write nr 2:"))
    print(nr1 * nr2)

if operation  == "/":
    nr1 = int(input("write nr 1:"))
    nr2 = int(input("write nr 2:"))
    print(nr1 / nr2)

if operation  == "**":
    nr1 = int(input("write nr 1:"))
    nr2 = int(input("write nr 2:"))
    print(nr1 ** nr2)

if operation  == "//":
    nr1 = int(input("write nr 1:"))
    nr2 = int(input("write nr 2:"))
    print(nr1 // nr2)

if operation  == "%":
    nr1 = int(input("write nr 1:"))
    nr2 = int(input("write nr 2:"))
    print(nr1 % nr2)